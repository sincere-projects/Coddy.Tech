# Operador Módulo
# a utilização de % para ter o resultado sendo da conta na divisão que sobra
# em outra palavras, aquilo que sobrar "o resto" na divisão, é o resultado.

a = 9
b = 2
c = 11
d = a % 2  # Resto 1
e = b % 3  # Resto 2
f = c % 10 # Resto 1

print(f"a = {a}")
print(f"b = {b}")
print(f"c = {c}")
print(f"d = {d}")
print(f"e = {e}")
print(f"f = {f}")

